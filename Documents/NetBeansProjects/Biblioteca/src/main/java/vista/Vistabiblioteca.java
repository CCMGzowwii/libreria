    private void mostrarcategorias() {
        DefaultTableModel model = (DefaultTableModel) categoriatable.getModel();
        model.setRowCount(0);

        ArrayList<Categoria> categorias = new ArrayList();
        CategoriaController categoria = new CategoriaController();
        List<Categoria> leerc = categoria.leerCategorias();
        categorias.addAll(leerc);

        Object columnas[] = new Object[2];
        for (Categoria c : categorias) {
            columnas[0] = c.getCategoria_id();
            columnas[1] = c.getNombre();

            model.addRow(columnas);
        }
    }

    private void mostrarusuarios() {
        DefaultTableModel model = (DefaultTableModel) usuariotable.getModel();
        model.setRowCount(0);

        ArrayList<Usuario> usuarios = new ArrayList();
        UsuarioController usuario = new UsuarioController();
        List<Usuario> leeru = usuario.leerUsuario();
        usuarios.addAll(leeru);

        Object columnas[] = new Object[4];
        for (Usuario u : usuarios) {
            columnas[0] = u.getId_usuario();
            columnas[1] = u.getNombre();
            columnas[2] = u.getEmail();
            columnas[3] = u.getTelefono();
            model.addRow(columnas);

        }
    }

    private void mostrarprestamos() {
        DefaultTableModel model = (DefaultTableModel) prestamotable.getModel();
        model.setRowCount(0);

        ArrayList<Prestamo> prestamos = new ArrayList();
        PrestamoController prestamo = new PrestamoController();
        List<Prestamo> leerp = prestamo.leerPrestamos();
        prestamos.addAll(leerp);

        Object columnas[] = new Object[5];
        for (Prestamo p : prestamos) {
            columnas[0] = p.getId_prestamo();
            columnas[1] = p.getId_libroprestamo();
            columnas[2] = p.getId_usuarioprestamo();
            columnas[3] = p.getFecha_prestamo();
            columnas[4] = p.getFecha_devolucion();
            model.addRow(columnas);
        }
    }

