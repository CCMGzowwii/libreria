    private void mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarActionPerformed
        DefaultTableModel model = (DefaultTableModel) librotable.getModel();
        model.setRowCount(0);

        ArrayList<Libro> libros = new ArrayList();
        LibroController libro = new LibroController();
        List<Libro> leer = libro.leerLibros();
        libros.addAll(leer);

        Object columnas[] = new Object[6];
        for (Libro l : libros) {
            columnas[0] = l.getId_libro();
            columnas[1] = l.getTitulo();
            columnas[2] = l.getAutor();
            columnas[3] = l.getEditorial();
            columnas[4] = l.getAnio_publicacion();
            columnas[5] = l.getIsbn();
            model.addRow(columnas);

        }

        nombrel.setVisible(false);
        autorl.setVisible(false);
        anol.setVisible(false);
        editoriall.setVisible(false);
        isbnl.setVisible(false);
        butoncrear.setVisible(false);
        botonactualizar.setVisible(false);
        idl.setVisible(false);
        ltitulo.setVisible(false);
        lautor.setVisible(false);
        leditorial.setVisible(false);
        lano.setVisible(false);
        lisbn.setVisible(false);
        lid.setVisible(false);

    }//GEN-LAST:event_mostrarActionPerformed

